package com.kh.java.basic;

public class Exam_Variable {
	
	public static void main(String[] args) {
		
		//변수의 선언
		int num;
		
		//변수의 초기화
		num = 123;
		
		// int num = 123;
		
		String str = "Hello World"; // 문자열
		
		System.out.println(111234);
		System.out.println(num);
		System.out.println(str);
		
		num = 321; // 변수는 바뀔 수 있음

		System.out.println(num);
	}

}
